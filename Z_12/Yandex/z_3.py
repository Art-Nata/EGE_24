"""Дана программа для Редактора:

НАЧАЛО
ПОКА нашлось (444) ИЛИ нашлось (333)
    ЕСЛИ нашлось (444)
        ТО заменить (444, 3)
        ИНАЧЕ заменить (333, 3344)
    КОНЕЦ ЕСЛИ
    ЕСЛИ нашлось (3443)
        ТО заменить (3443, 0)
    КОНЕЦ ЕСЛИ
КОНЕЦ ПОКА
КОНЕЦ
Какая сумма всех цифр в строке получится в результате применения этой программы к строке,
которая состоит из 50 идущих подряд цифр 4? В ответе запишите полученную сумму."""

st = 50 * '4'
while '444' in st or '333' in st:
    if '444' in st:
        st = st.replace('444', '3', 1)
    else:
        st = st.replace('333', '3344', 1)
    if '3443' in st:
        st = st.replace('3443', '0', 1)
print(st, sum([int(x) for x in st]))